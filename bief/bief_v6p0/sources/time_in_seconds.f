C                       ********************************
                        INTEGER FUNCTION TIME_IN_SECONDS
C                       ********************************
C
     *()
C
C***********************************************************************
C  BIEF VERSION 5.1         04/08/98  J-M HERVOUET (LNH) 01 30 87 80 18
C
C***********************************************************************
C
C      FONCTIONS: TEMPS DE LA MACHINE EN SECONDES
C      ==========
C
C-----------------------------------------------------------------------
C                             ARGUMENTS
C .________________.____.______________________________________________.
C |      NOM       |MODE|                   ROLE
C |________________|____|______________________________________________|
C |                |    |  
C |________________|____|______________________________________________|
C MODE : -->(DONNEE NON MODIFIEE), <--(RESULTAT), <-->(DONNEE MODIFIEE)
C
C-----------------------------------------------------------------------
C
C APPELE PAR :
C
C SOUS-PROGRAMMES APPELES : NEANT
C
C**********************************************************************
C
      IMPLICIT NONE
      INTEGER LNG,LU
      COMMON/INFO/LNG,LU
C
      INTEGER TEMPS,PARSEC
C
C----------------------------------------------------------------------
C
      CALL SYSTEM_CLOCK(COUNT=TEMPS,COUNT_RATE=PARSEC)
      TIME_IN_SECONDS = TEMPS / PARSEC
C
C----------------------------------------------------------------------
C
      RETURN
      END       
