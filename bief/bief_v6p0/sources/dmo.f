C             *****************************
              DOUBLE PRECISION FUNCTION DMO
C             *****************************
C
     *(T)
C
C***********************************************************************
C BIEF VERSION 5.1          12/07/95    E. DAVID (LHF) 76 33 42 08
C
C***********************************************************************
C
C     FONCTIONS :  CALCUL D'UN ANGLE MODULO 2 PI.
C
C-----------------------------------------------------------------------
C                             ARGUMENTS
C .________________.____.______________________________________________
C º      NOM       ºMODEº                   ROLE
C º________________º____º______________________________________________
C º   T            º -->º ANGLE.
C º________________º____º______________________________________________
C MODE : -->(DONNEE NON MODIFIEE), <--(RESULTAT), <-->(DONNEE MODIFIEE)
C-----------------------------------------------------------------------
C
C PROGRAMMES APPELES :
C
C-----------------------------------------------------------------------
C
      IMPLICIT NONE
C
      INTEGER LNG,LU
      COMMON/INFO/LNG,LU
C
C+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
C
      DOUBLE PRECISION, INTENT(IN) :: T
C
C+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
C
      DOUBLE PRECISION D
      INTRINSIC ACOS,MOD
C
C---------------------------------------------------------------
C
      D = MOD(T,360.D0)
      IF (D.LT.0.D0) D=D+360.D0
      DMO = D * ACOS (-1.D0) / 180.D0
C
C---------------------------------------------------------------
C
      RETURN
      END   
 
