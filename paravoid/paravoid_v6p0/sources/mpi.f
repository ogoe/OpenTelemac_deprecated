                        SUBROUTINE MPI_ALLTOALL
C                       ***********************
C
     *(I1,I2,I3,I4,I5,I6,I7,I8)
C
C***********************************************************************
C  PARA VERSION 5.9      18/08/2008   J-M HERVOUET (LNHE) 01 30 87 80 18
C
C***********************************************************************
C
C      FONCTIONS:
C      ==========
C
C-----------------------------------------------------------------------
C                             ARGUMENTS
C .________________.____.______________________________________________.
C |      NOM       |MODE|                   ROLE
C |________________|____|______________________________________________|
C |                | -->|  
C |________________|____|______________________________________________|
C MODE : -->(DONNEE NON MODIFIEE), <--(RESULTAT), <-->(DONNEE MODIFIEE)
C
C-----------------------------------------------------------------------
C
C APPELE PAR :
C
C SOUS-PROGRAMMES APPELES : MODULE STREAMLINE
C
C**********************************************************************
C
      IMPLICIT NONE
C
      INTEGER LNG,LU
      COMMON/INFO/LNG,LU
C
      INTEGER, INTENT(IN) :: I1(*),I2,I3,I4(*),I5,I6,I7,I8
C
C----------------------------------------------------------------------
C
      IF(LNG.EQ.1) WRITE(LU,*) 'APPEL DE MPI_ALLTOALL VERSION VIDE'
      IF(LNG.EQ.2) WRITE(LU,*) 'CALLING VOID MPI_ALLTOALL'
C
C----------------------------------------------------------------------
C
      RETURN
      END
